#include "Lock.h"

#include "Servo.h"
#include "Delay.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define DUTY_LOCK       30
#define DUTY_UNLOCK     20

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
void Control_Lock(uint8 *state) {
    switch(*state) {
        case LOCK:
            Set_Duty_Lock(DUTY_LOCK);
            break;
        case UNLOCK:
            Set_Duty_Lock(DUTY_UNLOCK);
            break;
        default:
            break;
    }
}
